// Klubb-spesifikk flytende logo
const FloatingLogo: React.FC<{ club: any; customLogo?: string }> = ({ club, customLogo }) => {
  // Sjekk om customLogo er en opplastet fil (data URL)
  const isUploadedLogo = customLogo && customLogo.startsWith('data:');
  
  // Hvis det er en opplastet logo (data URL), bruk den
  if (isUploadedLogo) {
    return (
      <img 
        src={customLogo} 
        alt={club.name}
        className="drop-shadow-[0_0_60px_rgba(255,255,255,0.5)]"
        style={{ 
          width: '400px',
          height: 'auto',
          maxWidth: '100%',
        }}
      />
    );
  }
  
  // For master bruker vi alltid den store inline SVG-logoen
  if (club.id === 'master') {
    return <VerticalLogo />;
  }
  
  // For andre klubber, bruk deres logo-fil fra clubSandbox.ts
  if (club.logos?.vertical) {
    return (
      <img 
        src={club.logos.vertical} 
        alt={club.name}
        className="drop-shadow-[0_0_60px_rgba(255,255,255,0.5)]"
        style={{ 
          width: '400px',
          height: 'auto',
          maxWidth: '100%',
        }}
      />
    );
  }
  
  // Fallback til standard Klubbnettside-logo
  return <VerticalLogo />;
};

